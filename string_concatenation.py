first_name = 'pat'
middle_name = 'senerez'
last_name = 'bungubung'

#python convention of string concatenation

print(f'{first_name} {middle_name} {last_name}')


#basic string concatenation
print(first_name + ' ' + middle_name + ' ' + last_name)

#string 
print("My name is: %s %s %s" % (first_name, middle_name, last_name))

#format function 
print("My name is: {} {} {}".format(first_name, middle_name, last_name))

